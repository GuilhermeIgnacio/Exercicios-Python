n = int(input('Digite um número para descobrir o seu dobro triplo e raiz quadrada: '))

n2 = n * 2  # Calculando o seu dobro
n3 = n * 3  # Calculando seu triplo
nr = n * n  # Calculando a raiz quadrada

print('Número: {} // Dobro: {} // Triplo: {} // Raiz Quadrada: {}'.format(n, n2, n3, nr))
